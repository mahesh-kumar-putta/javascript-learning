// Switch statement : can be an efficient replacement to many else if statements 


let day = "pizza";

switch(day){
  case 1:
    console.log(`Sunday`);
    break;
  case 2:
    console.log(`Monday`);
    break;
  case 3:
    console.log(`Tuesday`);
    break;
  default:
    console.log(`No match found for ${day}`)
}
